#include<iostream>
#include<math.h>
using namespace std;
int main()
{
int sec,hr,min;
cout << "enter the time period in seconds:" << endl;
cin >> sec;
min = sec/60;
hr = min/60;
cout << "hours:" << int(hr) << endl;
cout << "minutes:" << int(min%60) << endl;
cout << "seconds:" << int(sec%60) << endl;
return 0;
} 
